package main

import "fmt"

func main() {

	// Example of a String
	fmt.Println("Hola" + " I am " + "Manish!")
	// Example of an Integer
	fmt.Println("1 + 1 =", 1+1)
	// Example of a Float
	fmt.Println("7.0 / 3.0 ", 7.0/3.0)
	// Example of a Boolean
	fmt.Println(true && false)
	fmt.Println(true || false)
	fmt.Println(!true)
}